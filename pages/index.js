import Head from 'next/head'
import Image from 'next/image'
import { Inter } from '@next/font/google'
import styles from '@/styles/Home.module.css'
import axios from 'axios'
import { useState } from 'react'
import InputBox from '@/components/searchBox'

const inter = Inter({ subsets: ['latin'] })

export default function Home() {
  const [errorMessage, setErrorMessage] = useState('');
  const [data, setData] = useState({});
  const [location, setLocation] = useState('');
  const [weather, setWeather] = useState('');
  
  var apiKey = "8f203b16150b3debb67a311e1a97d4bf";
  var lang = "en";
  var units = "standard";
  var url = `https://api.openweathermap.org/data/2.5/weather?q=${location}&units=${units}&appid=${apiKey}&lang=${lang}`;

  const searchLocation = (event) => {
    if(event.key === "Enter"){
      axios.get(url)
        .then((response) => {
          console.clear();
          setData(response.data)
          console.log(response.data)
          setWeather(response.data.weather)
          setErrorMessage("")
        }).catch(err => {
          console.log(err)
          setErrorMessage("Please enter another location")
          setData({})
          setWeather('')
          
        })
        setLocation('');
    }
  }
  return (
    <>
      <Head>
        <title>Weather App</title>
        <meta name="  description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <main className={styles.main}>
        <InputBox location={location} searchLocation={searchLocation} setLocation={setLocation}/>
        {
          weather && weather.map((w, index) =>   {
            return(
              <div key={index} className="weather">
                <div className='location'> {data.name} </div>
                <div> {w.main} </div>
                <div>{w.description}</div>  
              </div>
            )
          })
        }
        <div className='errorMessage'>
        {errorMessage}
        </div>
      </main>
    </>
  )
}
